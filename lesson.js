// if/else statement 

let number = 10; 

// = erklærer en verdi
// == sjekker innhold 
// === sjekker innhold og datatype 
// < mindre enn / less than 
// > større enn / more than 
// & og / and 
// eller / either 


if (number === 10) {
console.log("Dette tallet er akkurat 10");
}

else if (number < 10) {
console.log ("Dette tallet er mindre enn 10")
}

else if (number > 10) { console.log("Dette tallet er større enn 10")
}

else {
console.log ("Dette tallet er IKKE 10")
}